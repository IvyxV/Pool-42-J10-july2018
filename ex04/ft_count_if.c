int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		count_one;

	i = 0;
	count_one = 0;
	while (tab[i][0] != '\0')
	{
		if ((*f)(tab[i]) == 1)
			count_one++;
		i++;
	}
	return (count_one);
}
